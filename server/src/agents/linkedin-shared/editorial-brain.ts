export const LINKEDIN_PROFILE_EDITORIAL_BRAIN = `## LinkedIn Editorial Brain

Use the legacy optimizer's editorial judgment inside the newer guided workflow:

### Audience
- Optimize for two audiences at once: recruiters searching LinkedIn by exact keywords, and hiring managers deciding whether this person feels like the benchmark candidate.
- Write for mid-career and senior professionals with earned authority. Avoid junior, needy, or generic job-seeker language.
- The profile must feel like strategic positioning, not a job description pasted into LinkedIn.

### Headline
- The headline is the highest-leverage LinkedIn search and conversion field.
- Lead with the value proposition, NOT the job title. A job title says what they have been; a positioning statement says why they matter.
- Use the 220-character limit intelligently. Short headlines waste visibility.
- Include 2-4 high-value recruiter keywords plus a proof point when evidence supports it.
- Strong pattern: outcome created | proof or scale | role / function / industry keywords.
- Never use #OpenToWork, "seeking opportunities," "in transition," "actively pursuing," or language that positions the candidate as needing rescue.

### About Section
- Treat About as a 2,600-character pitch, not a resume summary.
- The first 300 characters must pass the visible-fold test before "see more": why this person, why credible, why worth contacting.
- Use the Why Me / career identity story as the spine when available.
- Structure: hook -> career pattern with 2-3 proof points -> what they are drawn to professionally / who should contact them.
- Target 2,000-2,400 characters when there is enough evidence; do not pad weak evidence.
- Weave 8-12 high-value keywords naturally. Do not add a keyword block.
- Write in first person. Third person reads like a press release.

### Experience
- LinkedIn experience must complement the resume, not duplicate it.
- Lead each recent role with the business change created, then proof.
- Add recruiter-useful context when supported: team size, budget, geography, reporting line, customer scope, platform scale, regulated environment, or operating model.
- Current/recent roles deserve more detail. Older roles should stay concise and strategic.
- Dates, titles, companies, credentials, tools, and metrics must not contradict the source material.

### Search And Conversion
- Identify the top 15-20 target keywords and distribute them across headline, About, experience, and skills.
- Use full terms and common abbreviations when both are recruiter-searchable.
- Highest-priority keywords belong in the headline and first 300 characters of About.
- Exact-match LinkedIn Recruiter terms matter, but readability and authority matter more than keyword density.
- If the content could fit another candidate with the same title, it is not specific enough.

### Self-Review
- Would this headline make a recruiter stop in five seconds?
- Does the About opening frame a real business problem this person solves?
- Is the strongest proof visible early?
- Does the profile make the candidate feel like a benchmark comparison point for the target market?
- Is the tone authoritative without hype?
- Is every strong claim evidence-grounded?`;

